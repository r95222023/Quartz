    Encoding.prototype.compress = function (data) {
        return this.LZString.compressToUTF16(JSON.stringify(data));
    };

    Encoding.prototype.decompress = function (val) {
        if (!val || !val.compressed) return val;

        var decompressed, res;
        if (val.compressed) {
            decompressed = JSON.parse(this.LZString.decompressFromUTF16(val.compressed));
        }


        if (typeof decompressed === 'object' && !Array.isArray(decompressed)) {
            res = Object.assign({}, val, decompressed);
            delete res.compressed;
        } else {
            res = decompressed;
        }

        return res;
    };

    Encoding.prototype.md5 = function (content) {
        if (typeof content === 'string') {
            return this.MD5(content);
        } else {
            var sorted = sortObjectByPropery(content);
            return this.MD5(JSON.stringify(sorted))
        }
    };

    function sortObjectByPropery(obj) {
        if (Array.isArray(obj)) return obj;
        var keys = [],
            sortedObj = {};

        for (var prop in obj) {
            keys.push(prop);
        }

        keys.sort();

        for (var i = 0; i < keys.length; i++) {
            var key = keys[i];
            if (typeof obj[key] === 'object'&&obj[key]!==null) {
                sortedObj[key] = sortObjectByPropery(obj[key]);
            } else {
                sortedObj[key] = obj[key];
            }
        }
        return sortedObj;
    }
